package com.emg.tiange.HLink.Phone;

public class Presonal {
	public String mPresonName;
	public String mPresonNum;

	public Presonal(String PhoneName, String PhoneNum) {
		this.mPresonName = PhoneName;
		this.mPresonNum = PhoneNum;
	}

	public String getmPresonName() {
		return mPresonName;
	}

	public void setmPresonName(String mPresonName) {
		this.mPresonName = mPresonName;
	}

	public String getmPresonNum() {
		return mPresonNum;
	}

	public void setmPresonNum(String mPresonNum) {
		this.mPresonNum = mPresonNum;
	}
}
